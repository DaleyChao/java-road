package com.daleychao.algorithm.utils;

/**
 * @author Daniel
 */
public class PrintUtil {
    public static void printArray(Object[] array) {
        for (Object each : array) {
            System.out.print(each + " ");
        }
    }
    public static void printArrayInt(int[] array) {
        for (int each : array) {
            System.out.print(each + " ");
        }
    }
}
